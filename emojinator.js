function emojiParser(currentNode) {
  let cur = currentNode.innerHTML;
  if (cur) {
    cur = cur.replace(/[.,\/#!$%\^&\*;:{}=\-_`~()]/g, '');
    cur.split(' ').forEach((word) => {
      if (abridged[word.toLowerCase()]) {
        const re = new RegExp(word, 'i');
        currentNode.innerHTML = currentNode.innerHTML.replace(re, abridged[word.toLowerCase()]);
      }
    });
  }
  // base;
  if (!currentNode.childNodes) return;
  currentNode.childNodes.forEach((child) => {
    emojiParser(child);
  });
}

